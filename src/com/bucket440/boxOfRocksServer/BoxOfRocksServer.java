	public BoxOfRocksServer(Socket newClient, BRTheGarden newGarden){
	   
		client = newClient;
		theGarden = newGarden;
	}
	
 

	public void run(){
		//This gets the thread going for this client.  Shakes the hand, gets the rock,
		//etc... the tricky part about this was convincing each thread to use the
		//*same* instance of the Garden.
		try{
		   
		   out = new ObjectOutputStream(client.getOutputStream());
		   String welcome = "Welcome to BoxOfRocks Server";
		   System.out.println("Sending Welcome Message");
		   out.writeObject(welcome);
		   out.flush();
		   System.out.println("Receiving Rock From :" + client.getInetAddress());
		   
		   in = new ObjectInputStream(client.getInputStream());
		   thisRock = (BRAllRocks) in.readObject();
		   rockID = thisRock.getRockName() + " - " + thisRock.getOwnerName();
		   System.out.println("Remote user is: " + rockID);
		   theGarden.addRockToGarden(thisRock);
		   locationMap = theGarden.getLocationMap();
		   currentLocation = locationMap.get("Outside");
		   reloadGarden();
		   		   
		   readHeader();
		  		   
	   }catch(Exception e){
		   System.out.println(e);
		   closeAll();
	   }
	}
	
